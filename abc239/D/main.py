def solve(A: int, B: int, C: int, D: int):
    num_max = 200
    prime_table=[1]*(num_max+1)
    prime_table[0] = 0
    prime_table[1] = 0
    for i in range(2, num_max+1):
        k = i*2
        while k <= num_max:
            prime_table[k] = 0
            k += i
    ans = "Aoki"
    for i in range(A,B+1):
        tkahashi_win = True
        for j  in range(C,D+1):
            if prime_table[i+j]:
                tkahashi_win = False
                break
        if tkahashi_win:
            ans = "Takahashi"
            break
    print(ans)

def main():
    A = int(next(tokens))  # type: int
