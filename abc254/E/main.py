def sum_n_within_k(n,k):
    visited = set()
    q = deque()
    q.append((n,k))
    visited.add(n)
    while q:
        n,k = q.popleft()
        if k == 0:
            break
        for nxt in g[n]:
            if nxt not in visited:
                visited.add(nxt)
                q.append((nxt,k-1))
    return sum(visited)

